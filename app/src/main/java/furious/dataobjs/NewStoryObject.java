package furious.dataobjs;

public class NewStoryObject {

    public String NewsStoryID;
    public String TimeStamp;
    public String StoryAuthor;
    public String DistrictNumber;
    public String Title;
    public String URL;
    public String Description;
    public String GUID;
    public String PubDate;
    public String Category;
    public String ImageURL;
    public String ScrapeHash;
    public String TubeURL;
    public String MasterHash;
    public boolean isUC_Vid = false;



    public String getNewsStoryID() {
        return NewsStoryID;
    }

    public void setNewsStoryID(String newsStoryID) {
        NewsStoryID = newsStoryID;
    }

    public String getTimeStamp() {
        return TimeStamp;
    }

    public void setTimeStamp(String timeStamp) {
        TimeStamp = timeStamp;
    }

    public String getStoryAuthor() {
        return StoryAuthor;
    }

    public void setStoryAuthor(String storyAuthor) {
        StoryAuthor = storyAuthor;
    }

    public String getDistrictNumber() {
        return DistrictNumber;
    }

    public void setDistrictNumber(String districtNumber) {
        DistrictNumber = districtNumber;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public String getURL() {
        return URL;
    }

    public void setURL(String URL) {
        this.URL = URL;
    }

    public String getDescription() {
        return Description;
    }

    public void setDescription(String description) {
        Description = description;
    }

    public String getGUID() {
        return GUID;
    }

    public void setGUID(String GUID) {
        this.GUID = GUID;
    }

    public String getPubDate() {
        return PubDate;
    }

    public void setPubDate(String pubDate) {
        PubDate = pubDate;
    }

    public String getCategory() {
        return Category;
    }

    public void setCategory(String category) {
        Category = category;
    }

    public String getImageURL() {
        return ImageURL;
    }

    public void setImageURL(String imageURL) {
        ImageURL = imageURL;
    }

    public String getScrapeHash() {
        return ScrapeHash;
    }

    public void setScrapeHash(String scrapeHash) {
        ScrapeHash = scrapeHash;
    }

    public String getTubeURL() {
        return TubeURL;
    }

    public void setTubeURL(String tubeURL) {
        TubeURL = tubeURL;
    }

    public String getMasterHash() {
        return MasterHash;
    }

    public void setMasterHash(String masterHash) {
        MasterHash = masterHash;
    }

    public boolean isUC_Vid() {
        return isUC_Vid;
    }

    public void setUC_Vid(boolean UC_Vid) {
        isUC_Vid = UC_Vid;
    }


}
